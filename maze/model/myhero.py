"""Class Hero."""


class MyHero:
    """Create a hero."""

    def __init__(self):
        """Constructor Hero."""

    def hero_moves(self, move):
        """Hero moves."""
        if move == "left":
            print("Hero goes left.")
        if move == "right":
            print("Hero goes right.")
        if move == "up":
            print("Hero goes up.")
        if move == "down":
            print("Hero goes down")
